package net.bodz.lapiota.program.xmlfs;

import org.dom4j.DocumentType;
import org.dom4j.Element;
import org.dom4j.tree.DefaultDocument;

public class _Document
        extends DefaultDocument {

    private static final long serialVersionUID = 3165761413317284438L;

    public _Document() {
        super();
    }

    public _Document(DocumentType docType) {
        super(docType);
    }

    public _Document(Element rootElement, DocumentType docType) {
        super(rootElement, docType);
    }

    public _Document(Element rootElement) {
        super(rootElement);
    }

    public _Document(String name, Element rootElement, DocumentType docType) {
        super(name, rootElement, docType);
    }

    public _Document(String name) {
        super(name);
    }

}
